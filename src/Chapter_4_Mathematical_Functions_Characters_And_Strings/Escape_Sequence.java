
package Chapter_4;

public class Escape_Sequence {
    public static void main(String[] args) {
        
        System.out.println("The letter \"A\" is quoted");
        System.out.println("The word \"Ava 6ta iba\" is quoted");
        System.out.println("");
        // Examples
        System.out.println("\"\\b\" means Backspae: \b and happens?");
        System.out.println("\"\\t\" means Tab:  \t and happens?");
        System.out.println("\"\\n\" means Linefeed: \n and happens? \n");
        
        System.out.println("\"\\f\" means Formfeed: \f and happens?");
        System.out.println("\"\\r\" means Carriage Return: \r and happens? \"\\r\"");
        System.out.println("\"\\\\\" means Backslash: \\ and happens?");
        System.out.println("Double Quote: \" and happens?");
        
        
        
    }
    
}
