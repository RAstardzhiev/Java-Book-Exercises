
package Chapter_4;

public class CP_4_8_Char_Casting {
    public static void main(String[] args) {
        
        System.out.println("\"'1'\" is: " + (int)'1');
        System.out.println("\"'A'\" is: " + (int)'A');
        System.out.println("\"'B'\" is: " + (int)'B');
        System.out.println("\"'a'\" is: " + (int)'a');
        System.out.println("\"'b'\" is: " + (int)'b');
        
        System.out.println("");
        
        System.out.println("\"'40'\" is: " + (char)40);
        System.out.println("\"'59'\" is: " + (char)59);
        System.out.println("\"'79'\" is: " + (char)79);
        System.out.println("\"'85'\" is: " + (char)85);
        System.out.println("\"'90'\" is: " + (char)90);
        
        System.out.println("");
        
        // Hexadecimal... unexplained
        
    }
    
}
