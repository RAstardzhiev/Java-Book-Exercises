
package Chapter_4;

public class CheckPoint_4_7 {
    public static void main(String[] args) {
        
        System.out.println("Math.exp(5.5) = " + Math.exp(5.5));
        System.out.println("Math.log(5.5) = " + Math.log(5.5));
        
        System.out.println("Math.pow(5.5, 1.7) = " + Math.pow(5.5, 1.7));
        
        System.out.println("Math.log(Math.exp(5.5)) = " + Math.log(Math.exp(5.5)));
        System.out.println("Math.log(Math.exp(4.5)) = " + Math.log(Math.exp(4.5)));
        
        System.out.println("Math.pow(5.5, 5.5) = " + Math.pow(5.5, 5.5));
    }
    
}
